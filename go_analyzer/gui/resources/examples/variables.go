// Example demonstrating variable declarations and arithmetic
package main

import "fmt"

func main() {
    // Integer variables
    x := 10
    y := 20
    z := x + y

    // String variables
    name := "Go Analyzer"
    message := "Welcome to " + name

    // Boolean variables
    isActive := true

    // Output
    fmt.Println("Sum:", z)
    fmt.Println(message)
    fmt.Println("Active:", isActive)
}
